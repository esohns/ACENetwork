/* HTTP_Scanner_lex_destroy is for both reentrant and non-reentrant scanners. */
int HTTP_Scanner_lex_destroy  (yyscan_t yyscanner)
{
    struct yyguts_t * yyg = (struct yyguts_t*)yyscanner;

    /* Pop the buffer stack, destroying each element. */
	while(YY_CURRENT_BUFFER){
		HTTP_Scanner__delete_buffer(YY_CURRENT_BUFFER ,yyscanner );
		YY_CURRENT_BUFFER_LVALUE = NULL;
		HTTP_Scanner_pop_buffer_state(yyscanner);
	}

	/* Destroy the stack itself. */
	HTTP_Scanner_free(yyg->yy_buffer_stack ,yyscanner);
	yyg->yy_buffer_stack = NULL;

    /* Destroy the start condition stack. */
        HTTP_Scanner_free(yyg->yy_start_stack ,yyscanner );
        yyg->yy_start_stack = NULL;

    /* Reset the globals. This is important in a non-reentrant scanner so the next time
     * HTTP_Scanner_lex() is called, initialization will occur. */
    yy_init_globals( yyscanner);

/* %if-reentrant */
    /* Destroy the main struct (reentrant only). */
    HTTP_Scanner_free ( yyscanner , yyscanner );
    yyscanner = NULL;
/* %endif */
    return 0;
}
